/*
  Encriptar contraseñas con Bcrypt donde rondas son los saltos.
  Entre 7 y 10 saltos esta bien. Utilizare 7.

 */

function encriptar($input, $rounds) {
    $salt = "";
    $salt_chars = array_merge(range('A', 'Z'), range('a', 'z'), range(0, 9));
    for ($i = 0; $i < 22; $i++) {
        $salt .= $salt_chars[array_rand($salt_chars)];
    }
    return crypt($input, sprintf('$2a$%02d$', $rounds) . $salt);
}









